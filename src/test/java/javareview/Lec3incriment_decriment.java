package javareview;

public class Lec3incriment_decriment {

	public static void main(String[] args) {
		// lec3 increment decrement
// increment by 1 ex: i= i++ or i+1 
		
		int a = 10;
		a=a+1;
		System.out.println(a);
		
		// decrement by 1 ex: i= i-1 or i--
		int m=10;
		m= m-1;
		System.out.println(m);
		
		// another way of increment/decrement insted of i=i+1 or i=i-1
		int n =100;
		n+=1;
		System.out.println(n);
		
		int o=100;
		o-=1;
		System.out.println(o);
				
		
		
		
		
		
		
		
		
		
		
		
	}

}
